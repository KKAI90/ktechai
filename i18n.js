// ===== I18N ENGINE =====
const langConfig = {
  vi: { label: 'VI', flag: '🇻🇳', htmlLang: 'vi' },
  en: { label: 'EN', flag: '🇺🇸', htmlLang: 'en' },
  ja: { label: 'JP', flag: '🇯🇵', htmlLang: 'ja' },
};

let currentLang = localStorage.getItem('ktechai-lang') || 'vi';

function applyTranslations(lang) {
  const t = translations[lang];
  if (!t) return;
  currentLang = lang;
  localStorage.setItem('ktechai-lang', lang);

  // html lang attr
  document.documentElement.lang = langConfig[lang].htmlLang;
  document.title = t.pageTitle;
  document.querySelector('meta[name="description"]')?.setAttribute('content', t.pageDesc);

  // text nodes
  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.getAttribute('data-i18n');
    if (t[key] !== undefined) el.textContent = t[key];
  });

  // innerHTML (supports html tags)
  document.querySelectorAll('[data-i18n-html]').forEach(el => {
    const key = el.getAttribute('data-i18n-html');
    if (t[key] !== undefined) el.innerHTML = t[key];
  });

  // placeholders
  document.querySelectorAll('[data-i18n-ph]').forEach(el => {
    const key = el.getAttribute('data-i18n-ph');
    if (t[key] !== undefined) el.placeholder = t[key];
  });

  // update lang switcher active state
  document.querySelectorAll('.lang-btn').forEach(btn => {
    btn.classList.toggle('active', btn.dataset.lang === lang);
  });

  // update pricing data-type to re-render
  const activeTab = document.querySelector('.price-tab.active');
  if (activeTab) activeTab.click();
}

function initLangSwitcher() {
  const switcher = document.getElementById('langSwitcher');
  if (!switcher) return;
  Object.entries(langConfig).forEach(([code, cfg]) => {
    const btn = document.createElement('button');
    btn.className = 'lang-btn' + (code === currentLang ? ' active' : '');
    btn.dataset.lang = code;
    btn.innerHTML = `<span class="lang-flag">${cfg.flag}</span><span>${cfg.label}</span>`;
    btn.addEventListener('click', () => applyTranslations(code));
    switcher.appendChild(btn);
  });
}

document.addEventListener('DOMContentLoaded', () => {
  initLangSwitcher();
  applyTranslations(currentLang);
});
